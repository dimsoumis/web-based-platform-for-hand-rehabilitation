<p>Serious games are defined as games that do not have fun or enjoyment as their primary purpose. The digital games offered in our platform work as assessment tests and exercises for fine motor skills and gross manual dexterity. They require common pinches of everyday life to be performed, and they imitate grips and movements used in real life treatments.</p>
	
	<p>The applications of the platform can be used at any place, any time, if a midrange computer, a stable internet connection and a webcam are available. You are advised to use a 15fps camera or better, for optimal experience. Moreover, usage in a well lit room and distance of 50cm away from the camera is recommended.</p>
	
